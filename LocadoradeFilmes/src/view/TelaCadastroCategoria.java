    private void JFBotaoSalvarNovaCategoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JFBotaoSalvarNovaCategoriaActionPerformed
        String nomeCategoria = JFNomeNovaCategoria.getText();

        if (nomeCategoria.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nome da categoria n√£o pode estar vazio.");
            return;
        }

        try (Connection connection = ConnectionFactory.createConnection()) {
            String sql = "INSERT INTO categorias (nome) VALUES (?)";
            PreparedStatement stmt = connection.prepareStatement(sql);
            stmt.setString(1, nomeCategoria);
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Categoria salva com sucesso!");
            JFNomeNovaCategoria.setText("");

            
            if (telaCadastroFilmes != null) {
                telaCadastroFilmes.carregarCategorias();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao salvar categoria: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_JFBotaoSalvarNovaCategoriaActionPerformed

