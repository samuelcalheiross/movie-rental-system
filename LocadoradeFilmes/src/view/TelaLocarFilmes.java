    private void JFBotaoSalvarLocacaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JFBotaoSalvarLocacaoActionPerformed
        try {
            
            int indiceSelecionado = JFComboBoxLocacao.getSelectedIndex();
            if (indiceSelecionado == -1) {
                JOptionPane.showMessageDialog(this, "Selecione um filme.", "Aviso", JOptionPane.WARNING_MESSAGE);
                return;
            }

            Filme filme = filmesDisponiveis.get(indiceSelecionado);

            int quantidadeLocada = Integer.parseInt((String) JFComboBoxQuantidadeLocacao.getSelectedItem());

            if (filme.getQuantidade() < quantidadeLocada) {
                JOptionPane.showMessageDialog(this, "Quantidade locada maior que a disponível no estoque.", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }

            int novaQuantidade = filme.getQuantidade() - quantidadeLocada;

            FilmeDao filmeDao = new FilmeDao();
            filmeDao.atualizarQuantidade(filme.getId(), novaQuantidade);

            JOptionPane.showMessageDialog(this, "Filme locado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);

            carregarFilmes();

            TelaListaFilmes telaListaFilmes = new TelaListaFilmes();
            carregarTabelaFilmes(telaListaFilmes);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao locar filme: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_JFBotaoSalvarLocacaoActionPerformed

