    private void carregarFilmes() {
        try {
            
            JFComboBoxLocacao.removeAllItems();

            FilmeDao filmeDao = new FilmeDao();
            List<Filme> filmes = filmeDao.read();

            for (Filme filme : filmes) {
                JFComboBoxLocacao.addItem(filme.getNomeFilme());
            }

            this.filmesDisponiveis = filmes;

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar filmes: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void carregarTabelaFilmes(TelaListaFilmes telaListaFilmes) {
        try {
            
            FilmeDao filmeDao = new FilmeDao();

            List<Filme> filmes = filmeDao.read();

            javax.swing.table.DefaultTableModel modeloTabela = (javax.swing.table.DefaultTableModel) telaListaFilmes.getTabelaFilmes().getModel();

            modeloTabela.setRowCount(0);

            for (Filme filme : filmes) {
                modeloTabela.addRow(new Object[]{
                    filme.getNomeFilme(),
                    filme.getCategoria() != null ? filme.getCategoria().getNome() : "Sem Categoria",
                    filme.getDuracao(),
                    filme.getAnoLancamento(),
                    filme.getQuantidade()
                });
            }

        } catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao carregar filmes: " + e.getMessage());
        }
    }

